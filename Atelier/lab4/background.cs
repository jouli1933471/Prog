        static public void AfficherMaze(Niveau niveau, Program.Joueur player)
        {

            int largeur = 0;
            int hauteur = 0;
            char[,] tabMaze = new char[0, 0];
            if (niveau == Niveau.Facile)
            {
                tabMaze = NiveauFacile();
                largeur = 22;
                hauteur = 15;
            }
            else if (niveau == Niveau.Moyen)
            {
                tabMaze = NiveauMoyen();
                largeur = 31;
                hauteur = 21;
            }
            else
            {
                tabMaze = NiveauDifficile();
                largeur = 61;
                hauteur = 30;
            }

            for (int y = 0; y < hauteur; y++)
            {
                for (int x = 0; x < largeur; x++)
                {
                    if (tabMaze[y, x] == '$')
                    {
                        Console.ForegroundColor = ConsoleColor.Yellow;
                        Console.Write('$');
                        Console.ForegroundColor = ConsoleColor.White;
                    }
                    else if (tabMaze[y, x] == 'F')
                    {
                        Console.ForegroundColor = ConsoleColor.Red;
                        Console.Write('X');
                        Console.ForegroundColor = ConsoleColor.White;
                    }
                    else if (tabMaze[y, x] == 'V')
                    {
                        Console.ForegroundColor = ConsoleColor.Magenta;
                        Console.Write('¤');
                        Console.ForegroundColor = ConsoleColor.White;
                    }
                    else
                    {
                        Console.Write(tabMaze[y, x]);
                    }
                }

                Console.WriteLine("");


            }
            Console.ForegroundColor = ConsoleColor.Red;
            Console.WriteLine(" \n \nLives : " + player.vie);
            Console.ForegroundColor = ConsoleColor.Yellow;
            Console.WriteLine("Money : " + player.money + " $");
            Console.ForegroundColor = ConsoleColor.White;
        }


    }



